              <h2>Home loan Overview</h2>
            </div>
            <p>Home Loan is offered to individuals who wish to purchase or construct a house. The property is mortgaged to the lender as a security till the repayment of the Home Loan is done. The bank or financial institution will hold the title or deed to the property till the Home Loan has been paid back with the interest due for it.  The amount that can be financed typically depends on the status of the borrower (resident/non-resident), type of Home Loan (<a href="http://cfplindia.com/loan-against-property.php" class="text-link">renovation</a>, <a href="http://cfplindia.com/loan-against-property.php" class="text-link">property purchase</a>, <a href="http://cfplindia.com/loan-against-property.php" class="text-link">property extension</a>) and the financial institute.</p>
            <p>It is generally offered for up to 80-85% of the cost of the property. In a Home Loan, the owner of the property (the borrower) transfers the title to the lender (bank) on the condition that the title will be transferred back to the owner once the payment has been made and other terms of the Home Loan have been met.</p>
            
            <p>A Home Loan will have either a fixed or floating interest rate, which is paid monthly along with a contribution to the principal loan amount. As the homeowner pays down the principal over time, the interest is calculated on a smaller base so that future mortgage payments apply more towards principal reduction as opposed to just paying the interest charges.</p>
            
            <p>Home Loan allows a much broader group of citizens the chance to own a property, as the entire sum of the house doesn't have to be provided up front. But because the lender actually holds the title for as long as the mortgage is in effect, they have the right to foreclose the home (sell it on the open market) if the borrower can't make the payments. A Home Loan is one of the most common forms of debt, and it is also one of the most advised. Home Loans come with lower interest rates than almost any other kind of debt an individual consumer can find.</p>

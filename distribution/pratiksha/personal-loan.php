          <p>Unsecured Personal Loan is issued and supported only by the borrower's credit worthiness, rather than by a type of collateral. An Unsecured Personal Loan is one that is obtained without the use of property as collateral for the loan. Borrowers generally must have high credit ratings to be approved for an Unsecured Personal Loan.</p>

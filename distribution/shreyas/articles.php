				<div class="post_info_content">
                <p>Balance transfer of loan is the process where a customer transfers his outstanding principal amount to another bank or financial institute primarily for a better rate of interest and also better features. Almost every type of loan - auto, personal, home, education has a balance transfer facility and almost all banks have this facility.</p>
                <p>Balance transfer is a very lucrative facility for individuals who have taken a loan, but surprisingly very few avail of it. The problem is very few are aware of it. Every individual that has taken a loan should opt for a balance transfer at least once during the tenure of the loan. Balance transfer reduces you interest rates and finally enables you to save on the interest you have to pay. Also, income levels of an individual are dynamic and what you currently earn maybe more than what you earned two years ago.
                Balance transfer is a great facility that lets you reexamine your debt, make changes to it and also tweak it according to your requirements.</p>
                <p>Most banks also allow a top up loan on your balance transfer, which is particularly the case for personal loans and home loans. However, the prime motive behind balance transfer should be to reduce the burden of your debt and I would not advice one to go for a top up loan, unless absolutely necessary.</p>
                <p><strong>Why it makes sense:</strong></p>
                <p>For example if you have a personal loan for Rs. 2, 50,000 for tenure of 3 years at 20% interest rate, your monthly EMI comes to Rs. 9,291. The total interest you would pay for the duration of your loan will amount to Rs. 84,472. If after 1 year you go for a balance transfer and the rate of interest is now reduced to 14%, the principal outstanding on your loan is Rs. 1, 76,299. A balance transfer of the outstanding principle if rounded off to Rs. 1, 80,000, your monthly EMIs will drop to Rs. 8,642 while your total interest payable will drop to Rs. 27,416. For the first year you would have already paid back Rs. 44,039 which meant another Rs. 40,433 was remaining. But with balance transfer your total interest now stands at Rs. 27,416, which means a saving of about Rs. 13,000.</p>

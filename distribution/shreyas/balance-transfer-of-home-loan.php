            <h1>Balance Transfer of Home Loan Overview</h1>
          </div>
          <p>A <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loans</a> transfer (also known as refinancing or balance transfer) is an option that most individuals opt for to avail the benefit of lower interest rates prevalent in the market. Usually the existing borrower of a bank, who is about two or more years into his loan tenure, does not get the benefit of falling interest rates in the market. The Reserve Bank of India has been insisting on lower interest benefits to be passed on to the existing borrowers as well, but this seldom happens. </p>
          
          <p>Balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loans</a> happens when the entire unpaid principal loan amount is transferred to another bank for a lower rate of interest. The bank that had originally extended the loan to you gets the unpaid amount and you have to, in turn, now pay your EMIs at the new rate to the bank that has taken up the loan. Almost every bank in the country has a facility for a balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loans</a> and if you have been paying your EMIs regularly, there is often no problem associated with it.</p>
          
          <p>There is, however, a need to carry out a cost benefit analysis. Balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loan</a> will depend on the difference between the interest rates offered by the two banks, the amount of the loan left unpaid and the tenure remaining. If the unpaid amount is low or if only a few years remain in terms of tenure, balance transfer may not be ideal. This is also because banks often levy a processing fee for balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loans</a> and in the end it may not be all that beneficial.</p>
          
          <p>This is a substantial amount and despite the processing fee that you may have to incur on the balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loan</a> process. There is, however, a considerable amount of work involved for balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loan</a>. The bank where your loan will be transferred will do a credit background check and if they are not satisfied with your credit worthiness, there is every chance of your balance transfer of <a href="http://loaninmumbai.com/home-loan.php" class="text-link">Home Loans</a> being refused</p>
          
